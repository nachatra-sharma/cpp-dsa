// assignment operator

#include<iostream>
using namespace std;
int main() {
    int a = 10, b = 5, c = 12, d = 15, e = 101;

    a += 9;
    b -= 5;
    c *= 2;
    d /= 3;
    e %= 12;
    cout << a << endl;
    cout << b << endl;
    cout << c << endl;
    cout << d << endl;
    cout << e << endl;
}